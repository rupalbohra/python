# Create a list using []

a = [1, 2, 3, 4, 46, 78]

# Print the list using print() function:

print(a)

# Access using index a[0], a[1], a[2]

print(a[0])

# Changing values in the list:

a[0] = 98
print(a)

# We can create a list with items if different types

c = [45, "harry", 0.9, False]     # --> Hence list can store values of any data type
print(c)


